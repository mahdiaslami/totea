export function fakeDays(count = 10) {
  const startDate = new AppDate()
  startDate.addDays(-count / 2)

  return Array.from(
    { length: count },
    (_, i) => {
      const date = startDate.duplicate().addDays(i)

      return {
        date: date.toISOString(),
        tasks: fakeTasks(date, randomInt(20))
      }
    }
  )
}

function fakeTasks(date: AppDate, count: number) {
  return Array.from(
    { length: count },
    () => {
      return {
        id: uuid(),
        text: randomItem(texts),
        completedAt: randomBoolean() ? date.toISOString() : null
      }
    }
  )

}

function randomItem(arr: any[]) {
  return arr[randomInt(arr.length)]
}

function randomBoolean(trueRate = 50) {
  return randomInt(100) >= trueRate
}

function randomInt(max: number, min = 0) {
  return Math.floor(
    Math.random() * (max - min) + min
  )
}

